int	ft_isalpha(int c)
{
	int	rc;

	rc = 0;
	if (c >= 'a' && c <= 'z')
	{
		rc = 1;
	}
	else if (c >= 'A' && c <= 'Z')
	{
		rc = 1;
	}
	return (rc);
}
